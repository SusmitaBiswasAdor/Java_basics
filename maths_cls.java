public class maths_cls {
       public static void main(String[] args)
       {
              //Math class
              //Math class is a built-in class in java that provides methods to perform mathematical operations
              //Math class has methods 
              //like sqrt(), pow(), abs(), min(), max(), ceil(), floor(), round(), random()

              System.out.println("The square root of 25 is: "+Math.sqrt(25));
              System.out.println("The power of 2^3 is: "+Math.pow(2,3));
              System.out.println("The absolute value of -5 is: "+Math.abs(-5));
              System.out.println("The minimum value between 5 and 10 is: "+Math.min(5,10));
              System.out.println("The maximum value between 5 and 10 is: "+Math.max(5,10));
              System.out.println("The ceil value of 5.5 is: "+Math.ceil(5.5));
              System.out.println("The floor value of 5.5 is: "+Math.floor(5.5));
              System.out.println("The round value of 5.5 is: "+Math.round(5.5));
              System.out.println("The random value between 0 and 1 is: "+Math.random());
              System.out.println((int)(Math.random()*100));

       }
       
}
